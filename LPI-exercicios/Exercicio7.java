public class Exercicio7{
    int numero1 = 3;
    int numero2 = 3;
    
    public void soma(){
        int resultado = numero1 + numero2;
        System.out.println(resultado);
    }

    public void subtracao(){
        int resultado = numero1 - numero2;
        System.out.println(resultado);
    }

    public void multiplicacao(){
        int resultado = numero1 * numero2;
        System.out.println(resultado);
    }

    public void divisao(){
        int resultado = numero1 / numero2;
        System.out.println(resultado);
    }
}